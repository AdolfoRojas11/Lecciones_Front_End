

var numero1 = 8;
var numero2 = 7;
var operacion = "*";

if(operacion == "+"){
    console.log("El resultado es:", numero1+numero2);
}
else if(operacion == "-"){
    console.log("El resultado es:", numero1-numero2);
}
else if(operacion == "*"){
    console.log("El resultado es:", numero1*numero2);
}
else {
    console.log("El resultado es:", numero1/numero2);
}

